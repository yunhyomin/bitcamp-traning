package day0628;
// 기본형 데이터타입 04
// 논리형

// 논리형 데이터타입에는 딱 한개의 데이터타입이 있는데 바로
// boolean이다.
// boolean에는 딱 두가지 값만 있는데 true 혹은 false 이다.

// boolean 자체도 우리가 해당 데이터타입의 변수를 직접 만들어서 쓰기 보다는
// 우리가 나중에 배우게 될 연산자 라는 것을 사용하여 그 연산의 결과값을 그대로
// 쓰거나 메소드의 결과값을 그대로 쓰는 경우가 더 많다.
public class Ex09Boolean {
    public static void main(String[] args) {
        // 1. boolean 데이터타입의 변수 myBool 을 선언해보자
        boolean myBool;
        // 2. myBool에 true를 할당해보자
        myBool = true;
        // 3. 화면에 myBool을 출력해보자
        System.out.println(myBool);
        // 4. myBool에 false를 할당해보자
        myBool = false;
        // 5. 화면에 myBool을 출력해보자
        System.out.println(myBool);
    }
}
















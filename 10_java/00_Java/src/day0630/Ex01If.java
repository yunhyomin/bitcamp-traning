// 변수/상수의 유효범위(scope)
//변수 혹은 상수는 유효범위라는것이 존재해서 해당 유효범위를 벗어나면 더이상 그 변수 혹은 상수는 
//존재하지 않는 것이 되어서 호출하거나 값을 넣어줄 수 없다.
//또한 해당 변수 혹은 상수가 유효한 범위안에서는 똑같은 이름의 변수 혹은 상수는 또 선언할 수 없다!

//예시:
//  int number = 4; 
//  int number = 6;
//  System.out.println(number) ->얘는 무엇을 출력해야 할까요?6

//변수 혹은 상수의 유효범위는 해당 변수 혹은 상수가 선언된 순간부터 
//그 변수/상수가 속해있는 코드 블락({})이 끝날때까지이다!

public class Ex01If {
    public static void main(String[] args) {
        //1.int 변수 number선언
        int number;
        
        //2.number를 7로 초기화 한다.
        number = 35;
        
        //3.if문을 통해서 number가 10보다 작으면 "10보다 작습니다"를 출력
        if(number<10) {
            System.out.println("10보다 작습니다.");
        }
        //4.프로그램이 종료되었다를 출력
        System.out.println("프로그램 종료");
        
        
    }
}

//예시: 회원가입시, 중복된 아이디를 넣으면 "중복된 아이디입니다."라고 출력이 되고 회원가입불가.

//제어문에는 크게 조건문과 반복문이 있는데 각각 다음과 같은 성격을 지닌다.
//조건문: 특정 조건식을 만족하면, 특정 코드들을 실행
//      크게 If조건문과 switch조건문이 있다.
//반복문: 특정 조건식을 만족하는동안 특정 코드들을 반복

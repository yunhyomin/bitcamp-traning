//if 조건문
//true/false가 나오는 조건식을 실행시켜서 조건식이 true가 나오면 특정코드들을 실행시키고.
//만약 false가 나오면 실행시키지 않는 제어문이다.

//if문은 다음과 같은 구조를 가진다.
